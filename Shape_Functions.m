function [Ni,Bi] = Shape_Functions(m,zhi)
if m == 2
    N1 = 0.5.*(1-zhi);
    N2 = 0.5.*(1+zhi);
    Ni = [N1;N2];
    B1 = -0.5;
    B2 = 0.5;
    Bi = [B1;B2];
elseif m == 3
    N1 = -0.5.*(1-zhi).*zhi;
    N2 = (1-zhi).*(1+zhi);
    N3 = 0.5.*zhi.*(1+zhi);
    Ni = [N1;N2;N3];
    B1 = -0.5.*(1-(2.*zhi));
    B2 = -2.*zhi;
    B3 = 0.5.*(1+(2.*zhi));
    Bi = [B1;B2;B3];
elseif m == 4
    N1 = (-9/16).*(1-zhi).*((1/3)-zhi).*((1/3)+zhi);
    N2 = (27/16).*(1-zhi).*((1/3)-zhi).*(1+zhi);
    N3 = (27/16).*(1-zhi).*((1/3)+zhi).*(1+zhi);
    N4 = (-9/16).*(1+zhi).*((1/3)-zhi).*((1/3)+zhi);
    Ni = [N1;N2;N3;N4];
    B1 = (-9/16).*((3.*(zhi.^2))-(2.*zhi)-(1/9));
    B2 = (27/16).*((3.*(zhi.^2))-((2/3).*zhi)-(1));
    B3 = (27/16).*((-3.*(zhi.^2))-((2/3).*zhi)+(1));
    B4 = (-9/16).*((-3.*(zhi.^2))-(2.*zhi)+(1/9));
    Bi = [B1;B2;B3;B4];
end
end